BEGIN
    EXECUTE IMMEDIATE 'DROP TABLE PEV_T_LOV_PROJECT_STATUS CASCADE CONSTRAINTS';
EXCEPTION
    WHEN OTHERS THEN
        NULL;
END;
/
CREATE TABLE PEV_T_LOV_PROJECT_STATUS (
id integer GENERATED BY DEFAULT ON NULL AS IDENTITY,
status_name VARCHAR2(30 CHAR),
status_type char, -- P - status zobrazující se u hlavieky projektu
is_deleted integer default 0,
CREATED_DATE DATE,
CREATED_BY VARCHAR2(30 CHAR),
UPDATED_DATE DATE,
UPDATED_BY VARCHAR2(30 CHAR),
CONSTRAINT PK_PROJECT_STATUS PRIMARY KEY (id)
);
COMMENT ON TABLE PEV_T_LOV_PROJECT_STATUS IS 'Ciselnik pro statusy projektu';
COMMENT ON COLUMN PEV_T_LOV_PROJECT_STATUS.id IS 'PK - ID Identifikátor';
COMMENT ON COLUMN PEV_T_LOV_PROJECT_STATUS.status_name IS 'Název statusu';
COMMENT ON COLUMN PEV_T_LOV_PROJECT_STATUS.status_type IS 'Typ statusu ( P - projekt, null - detail )';
COMMENT ON COLUMN PEV_T_LOV_PROJECT_STATUS.is_deleted IS 'Příznak smazání záznamu (1 - smazán)';
COMMENT ON COLUMN PEV_T_LOV_PROJECT_STATUS.UPDATED_DATE IS 'datum ulozeni';
COMMENT ON COLUMN PEV_T_LOV_PROJECT_STATUS.UPDATED_BY IS 'Uživatel, který uložil záznam.';
COMMENT ON COLUMN PEV_T_LOV_PROJECT_STATUS.CREATED_DATE IS 'datum vytvoření';
COMMENT ON COLUMN PEV_T_LOV_PROJECT_STATUS.CREATED_BY IS 'Uživatel, který vytvoril záznam.';
/

