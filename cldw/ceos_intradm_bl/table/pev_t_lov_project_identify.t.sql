CREATE TABLE PEV_T_LOV_PROJECT_IDENTIFY (
id integer GENERATED BY DEFAULT ON NULL AS IDENTITY,
name VARCHAR2(50 CHAR),
is_deleted integer default 0,
CREATED_DATE DATE,
CREATED_BY VARCHAR2(30 CHAR),
UPDATED_DATE DATE,
UPDATED_BY VARCHAR2(30 CHAR),
CONSTRAINT PK_PROJECT_IDENTIFICATION PRIMARY KEY (id)
);
COMMENT ON TABLE PEV_T_LOV_PROJECT_IDENTIFY IS 'Ciselnik pro identifikace projektu';
COMMENT ON COLUMN PEV_T_LOV_PROJECT_IDENTIFY.UPDATED_DATE IS 'datum ulozeni';
COMMENT ON COLUMN PEV_T_LOV_PROJECT_IDENTIFY.UPDATED_BY IS 'Uživatel, který uložil záznam.';
COMMENT ON COLUMN PEV_T_LOV_PROJECT_IDENTIFY.CREATED_DATE IS 'datum vytvoření';
COMMENT ON COLUMN PEV_T_LOV_PROJECT_IDENTIFY.CREATED_BY IS 'Uživatel, který vytvoril záznam.';
COMMENT ON COLUMN PEV_T_LOV_PROJECT_IDENTIFY.id IS 'PK - ID Identifikátor';
COMMENT ON COLUMN PEV_T_LOV_PROJECT_IDENTIFY.name IS 'Identifikační Název';
COMMENT ON COLUMN PEV_T_LOV_PROJECT_IDENTIFY.is_deleted IS 'Příznak smazání záznamu (1 - smazán)';
/
