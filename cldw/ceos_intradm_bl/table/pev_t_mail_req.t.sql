BEGIN
    EXECUTE IMMEDIATE 'DROP TABLE PEV_T_MAIL_REQ CASCADE CONSTRAINTS';
EXCEPTION
    WHEN OTHERS THEN
        NULL;
END;
/
CREATE TABLE PEV_T_MAIL_REQ(
ID integer GENERATED BY DEFAULT ON NULL AS IDENTITY,
CHANGE_ID integer,
PEOPLE_ID integer,
EMAIL varchar(100 CHAR),     -- NAME, SURNAME, ETC...
STATUS integer,
CREATED_DATE DATE,
CREATED_BY VARCHAR2(30 CHAR),
UPDATED_DATE DATE,
UPDATED_BY VARCHAR2(30 CHAR)
);
COMMENT ON TABLE PEV_T_MAIL_REQ IS 'Tabulka naplánovaných emailů pro odeslání';
COMMENT ON COLUMN PEV_T_MAIL_REQ.UPDATED_DATE IS 'Datum uložení';
COMMENT ON COLUMN PEV_T_MAIL_REQ.UPDATED_BY IS 'Uživatel, který uložil záznam.';
COMMENT ON COLUMN PEV_T_MAIL_REQ.CREATED_DATE IS 'Datum vytvoření';
COMMENT ON COLUMN PEV_T_MAIL_REQ.CREATED_BY IS 'Uživatel, který vytvoril záznam.';
COMMENT ON COLUMN PEV_T_MAIL_REQ.ID IS 'PK - ID Identifikátor';
COMMENT ON COLUMN PEV_T_MAIL_REQ.CHANGE_ID IS 'UD změny v pev_t_changes';
COMMENT ON COLUMN PEV_T_MAIL_REQ.PEOPLE_ID IS 'ID osoby pro odesílaný email';
COMMENT ON COLUMN PEV_T_MAIL_REQ.EMAIL IS ' Emailová adres';
COMMENT ON COLUMN PEV_T_MAIL_REQ.STATUS IS 'Stav odesílání 1-Vytvořen, 2-Odeslán';
/

