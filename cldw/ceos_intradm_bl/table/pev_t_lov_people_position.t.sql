CREATE TABLE PEV_T_LOV_PEOPLE_POSITION (
id integer GENERATED BY DEFAULT ON NULL AS IDENTITY,
name VARCHAR2(30 CHAR),
is_deleted integer default 0,
CREATED_DATE DATE,
CREATED_BY VARCHAR2(30 CHAR),
UPDATED_DATE DATE,
UPDATED_BY VARCHAR2(30 CHAR),
CONSTRAINT PK_PEOPLE_POSITION PRIMARY KEY (id)
);
COMMENT ON TABLE PEV_T_LOV_PEOPLE_POSITION IS 'Ciselnik pracovnich zarazeni internich osob';
COMMENT ON COLUMN PEV_T_LOV_PEOPLE_POSITION.UPDATED_DATE IS 'datum ulozeni';
COMMENT ON COLUMN PEV_T_LOV_PEOPLE_POSITION.UPDATED_BY IS 'Uživatel, který uložil záznam.';
COMMENT ON COLUMN PEV_T_LOV_PEOPLE_POSITION.CREATED_DATE IS 'datum vytvoření';
COMMENT ON COLUMN PEV_T_LOV_PEOPLE_POSITION.CREATED_BY IS 'Uživatel, který vytvoril záznam.';
COMMENT ON COLUMN PEV_T_LOV_PEOPLE_POSITION.id IS 'PK - ID Identifikátor';
COMMENT ON COLUMN PEV_T_LOV_PEOPLE_POSITION.name IS 'Název vlastníka';
COMMENT ON COLUMN PEV_T_LOV_PEOPLE_POSITION.is_deleted IS 'Příznak smazání záznamu (1 - smazán)';
/
