BEGIN
    EXECUTE IMMEDIATE 'DROP TABLE PEV_T_MAIL_SCHEDULER CASCADE CONSTRAINTS';
EXCEPTION
    WHEN OTHERS THEN
        NULL;
END;
/
CREATE TABLE PEV_T_MAIL_SCHEDULER(
ID integer GENERATED BY DEFAULT ON NULL AS IDENTITY,
PROJECT_ID INTEGER,
MAIL_FROM  VARCHAR2 (100 CHAR),
MAIL_TO VARCHAR2 (100 CHAR),
MAIL_CC VARCHAR2 (100 CHAR),
MAIL_SUBJECT  VARCHAR2 (100 CHAR),
MAIL_BODY CLOB,
SEND_DATE DATE,
STATUS integer,
CREATED_DATE DATE,
CREATED_BY VARCHAR2(30 CHAR),
UPDATED_DATE DATE,
UPDATED_BY VARCHAR2(30 CHAR)
);
COMMENT ON TABLE PEV_T_MAIL_SCHEDULER IS 'Tabulka emailů pro odeslání';
COMMENT ON COLUMN PEV_T_MAIL_SCHEDULER.UPDATED_DATE IS 'Datum uložení';
COMMENT ON COLUMN PEV_T_MAIL_SCHEDULER.UPDATED_BY IS 'Uživatel, který uložil záznam.';
COMMENT ON COLUMN PEV_T_MAIL_SCHEDULER.CREATED_DATE IS 'Datum vytvoření';
COMMENT ON COLUMN PEV_T_MAIL_SCHEDULER.CREATED_BY IS 'Uživatel, který vytvoril záznam.';
COMMENT ON COLUMN PEV_T_MAIL_SCHEDULER.ID IS 'PK - ID Identifikátor';
COMMENT ON COLUMN PEV_T_MAIL_SCHEDULER.PROJECT_ID IS 'ID Projektu';
COMMENT ON COLUMN PEV_T_MAIL_SCHEDULER.MAIL_FROM IS 'Odesilatel';
COMMENT ON COLUMN PEV_T_MAIL_SCHEDULER.MAIL_TO IS 'Příjemce';
COMMENT ON COLUMN PEV_T_MAIL_SCHEDULER.MAIL_CC IS 'Příjemce - Kopie';
COMMENT ON COLUMN PEV_T_MAIL_SCHEDULER.MAIL_SUBJECT IS 'Předmět zprávy';
COMMENT ON COLUMN PEV_T_MAIL_SCHEDULER.MAIL_BODY IS 'Tělo zprávy';
COMMENT ON COLUMN PEV_T_MAIL_SCHEDULER.STATUS IS 'Stav odesílání 1-Vytvořen, 2-Odeslán';
/

