CREATE TABLE PEV_T_LOV_PROJECT_STATE (
id integer GENERATED BY DEFAULT ON NULL AS IDENTITY,
state_name VARCHAR2(30 CHAR),
is_deleted integer  default 0,
CREATED_DATE DATE,
CREATED_BY VARCHAR2(30 CHAR),
UPDATED_DATE DATE,
UPDATED_BY VARCHAR2(30 CHAR),
CONSTRAINT PK_PROJECT_STATE PRIMARY KEY (id)
);
COMMENT ON TABLE PEV_T_LOV_PROJECT_STATE IS 'Ciselnik pro stavy projektu';
COMMENT ON COLUMN PEV_T_LOV_PROJECT_STATE.id IS 'PK - ID Identifikátor';
COMMENT ON COLUMN PEV_T_LOV_PROJECT_STATE.state_name IS 'Název postoja';
COMMENT ON COLUMN PEV_T_LOV_PROJECT_STATE.is_deleted IS 'Příznak smazání záznamu (1 - smazán)';
COMMENT ON COLUMN PEV_T_LOV_PROJECT_STATE.UPDATED_DATE IS 'datum ulozeni';
COMMENT ON COLUMN PEV_T_LOV_PROJECT_STATE.UPDATED_BY IS 'Uživatel, který uložil záznam.';
COMMENT ON COLUMN PEV_T_LOV_PROJECT_STATE.CREATED_DATE IS 'datum vytvoření';
COMMENT ON COLUMN PEV_T_LOV_PROJECT_STATE.CREATED_BY IS 'Uživatel, který vytvoril záznam.';
/

